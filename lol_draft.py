    def _on_champ_click(self, name):
        if self.assign_mode == "ban_blue":
            if name in self.bans_blue or name in self.bans_red: return
            if name in self.ally.values() or name in self.enemy.values(): return
            if len(self.bans_blue) >= 5: return
            self._push_undo()
            self.bans_blue.append(name)
            self._refresh()
            return
        if self.assign_mode == "ban_red":
            if name in self.bans_blue or name in self.bans_red: return
            if name in self.ally.values() or name in self.enemy.values(): return
            if len(self.bans_red) >= 5: return
            self._push_undo()
            self.bans_red.append(name)
            self._refresh()
            return

        target = self.ally if self.assign_mode == "ally" else self.enemy
        other = self.enemy if self.assign_mode == "ally" else self.ally

        if name in [v for v in other.values() if v]: return
        if name in self.bans_blue + self.bans_red: return

        possible_roles = CHAMPS_ROLES.get(name, [])
        if not possible_roles:
            possible_roles = ROLES

        if len(possible_roles) == 1:
            role = possible_roles[0]
            self._push_undo()
            if name in target.values():
                for r, v in target.items():
                    if v == name: target[r] = ""
            target[role] = name
            self._refresh()
        else:
            self._show_role_picker(name, possible_roles, target)

