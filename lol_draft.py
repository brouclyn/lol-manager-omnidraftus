    def _push_undo(self):
        state = {
            "ally": dict(self.ally),
            "enemy": dict(self.enemy),
            "bans_blue": list(self.bans_blue),
            "bans_red": list(self.bans_red),
        }
        self.undo_stack.append(state)
        if len(self.undo_stack) > 50:
            self.undo_stack.pop(0)
        self.redo_stack.clear()

    def _restore(self, state):
        self.ally = dict(state["ally"])
        self.enemy = dict(state["enemy"])
        self.bans_blue = list(state["bans_blue"])
        self.bans_red = list(state["bans_red"])

    def _undo(self):
        if not self.undo_stack: return
        cur = {
            "ally": dict(self.ally), "enemy": dict(self.enemy),
            "bans_blue": list(self.bans_blue), "bans_red": list(self.bans_red),
        }
        self.redo_stack.append(cur)
        state = self.undo_stack.pop()
        self._restore(state)
        self._refresh()

    def _redo(self):
        if not self.redo_stack: return
        cur = {
            "ally": dict(self.ally), "enemy": dict(self.enemy),
            "bans_blue": list(self.bans_blue), "bans_red": list(self.bans_red),
        }
        self.undo_stack.append(cur)
        state = self.redo_stack.pop()
        self._restore(state)
        self._refresh()

    def _clear_all(self):
        self._push_undo()
        for r in ROLES:
            self.ally[r] = ""
            self.enemy[r] = ""
        self.bans_blue.clear()
        self.bans_red.clear()
        self._refresh()

