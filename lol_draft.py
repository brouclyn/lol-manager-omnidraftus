def champs_for_role(role):
    return sorted([c for c, rs in CHAMPS_ROLES.items() if role in rs])

def get_counter(a, b):
    return COUNTERS.get(a, {}).get(b, 0)

def matchup_score(ally, enemy):
    return get_counter(ally, enemy) - get_counter(enemy, ally)

def get_synergy(a, b):
    return SYNERGIES.get(a, {}).get(b, 0)

def eval_team(team, enemies):
    score = 0.0
    names = [v for v in team.values() if v]
    enemy_names = [v for v in enemies.values() if v]
    lane_matchups = []
    for role in ROLES:
        ally = team.get(role, "")
        if not ally: continue
        enemy = enemies.get(role, "")
        if enemy:
            m = matchup_score(ally, enemy)
            score += m * 1.3
            lane_matchups.append(m)
        for er, en in enemies.items():
            if er != role and en:
                score += matchup_score(ally, en) * 0.7
    for i in range(len(names)):
        for j in range(i + 1, len(names)):
            score += get_synergy(names[i], names[j]) * 1.8
    if enemy_names and names:
        for a in names:
            for b in enemy_names:
                score -= get_synergy(b, a) * 0.4
    if lane_matchups:
        worst = min(lane_matchups)
        if worst <= -3: score -= 4
        elif worst <= -2: score -= 1.5
    return round(score, 1)

def optimize(enemies, locked, banned, iterations=5000):
    avail = {}
    for role in ROLES:
        if locked.get(role):
            avail[role] = [locked[role]]
        else:
            avail[role] = [c for c in champs_for_role(role)
                           if c not in banned
                           and c not in enemies.values()
                           and c not in locked.values()]
            if not avail[role]: avail[role] = [""]

    def gen():
        sol, used = {}, set()
        for role in ROLES:
            opts = [c for c in avail[role] if c not in used]
            if opts:
                sol[role] = random.choice(opts)
                used.add(sol[role])
            else:
                sol[role] = ""
        return sol

    best = gen()
    best_score = eval_team(best, enemies)
    for i in range(iterations):
        temp = 1 - i / iterations
        cand = dict(best)
        mr = [r for r in ROLES if not locked.get(r)]
        if not mr: break
        role = random.choice(mr)
        used = set(v for r, v in cand.items() if r != role)
        opts = [c for c in avail[role] if c not in used]
        if opts:
            cand[role] = random.choice(opts)
        cs = eval_team(cand, enemies)
        d = cs - best_score
        if d > 0 or random.random() < math.exp(d / (temp * 4 + 0.1)):
            best, best_score = cand, cs
    return best, best_score

def suggest_bans(enemies, ally_picks, banned_and_picked):
    cands = [n for n in ALL_CHAMPS if n not in banned_and_picked]
    threats = []
    for e in cands:
        t = 0.0
        counter_count = len(COUNTERS.get(e, {}))
        avg_counter = sum(COUNTERS.get(e, {}).values()) / max(1, counter_count)
        t += counter_count * 0.15 + avg_counter * 0.5
        for ally in ally_picks.values():
            if ally:
                t += get_counter(e, ally) * 1.8
                t -= get_counter(ally, e) * 0.8
        syn_count = len(SYNERGIES.get(e, {}))
        t += syn_count * 0.1
        threats.append((e, round(t, 1)))
    threats.sort(key=lambda x: -x[1])
    return threats[:12]


class PortraitLoader:
    def __init__(self):
        self.cache = {}
        self.banned_cache = {}
        self.dimmed_cache = {}
        os.makedirs(CACHE_DIR, exist_ok=True)

    def _fallback(self, name, size):
        if not PIL_OK:
            return None
        img = Image.new("RGB", (size, size), self._color_for(name))
        d = ImageDraw.Draw(img)
        short = to_display(name)[:6]
        try:
            font = ImageFont.truetype("arial.ttf", max(10, size // 4))
        except:
            font = ImageFont.load_default()
        bbox = d.textbbox((0, 0), short, font=font)
        w, h = bbox[2] - bbox[0], bbox[3] - bbox[1]
        d.text(((size - w) // 2, (size - h) // 2 - 2), short, fill=(0, 0, 0), font=font)
        return img

    def _color_for(self, name):
        h = sum(ord(c) for c in name)
        palette = [(200, 155, 60), (123, 74, 158), (42, 111, 184), (138, 90, 58),
                   (160, 69, 69), (74, 144, 200), (122, 138, 74), (154, 58, 58),
                   (106, 74, 42), (74, 154, 138), (138, 74, 106), (200, 160, 74)]
        return palette[h % len(palette)]

    def _load_one(self, name, size):
        key = f"{name}_{size}"
        if key in self.cache:
            return self.cache[key]

        cache_path = os.path.join(CACHE_DIR, f"{name}.png")
        img = None

        if os.path.exists(cache_path):
            try:
                img = Image.open(cache_path).convert("RGB")
            except:
                img = None

        if img is None:
            url = f"https://ddragon.leagueoflegends.com/cdn/{DDRAGON_VERSION}/img/champion/{name}.png"
            try:
                req = urllib.request.Request(url, headers={"User-Agent": "Mozilla/5.0"})
                with urllib.request.urlopen(req, timeout=5) as r:
                    raw = r.read()
                img = Image.open(BytesIO(raw)).convert("RGB")
                try:
                    img.save(cache_path, "PNG")
                except:
                    pass
            except Exception:
                img = self._fallback(name, size)

        if img is None:
            return None

        if img.size != (size, size):
            img = img.resize((size, size), Image.LANCZOS)

        tk_img = ImageTk.PhotoImage(img)
        self.cache[key] = tk_img
        return tk_img

    def get(self, name, size=56):
        if not PIL_OK:
            return None
        return self._load_one(name, size)

    def get_banned(self, name, size=40):
        if not PIL_OK: return None
        key = f"{name}_{size}_ban"
        if key in self.banned_cache:
            return self.banned_cache[key]
        base = self._load_one(name, size)
        if not base: return None
        cache_path = os.path.join(CACHE_DIR, f"{name}.png")
        try:
            img = Image.open(cache_path).convert("RGB")
            img = img.resize((size, size), Image.LANCZOS)
        except:
            img = self._fallback(name, size)
        img = img.convert("RGBA")
        overlay = Image.new("RGBA", (size, size), (0, 0, 0, 140))
        img = Image.alpha_composite(img, overlay)
        d = ImageDraw.Draw(img)
        d.line((4, 4, size - 4, size - 4), fill=(232, 76, 76, 255), width=3)
        tk_img = ImageTk.PhotoImage(img.convert("RGB"))
        self.banned_cache[key] = tk_img
        return tk_img

    def get_dimmed(self, name, size=64, color="ally"):
        if not PIL_OK: return None
        key = f"{name}_{size}_{color}"
        if key in self.dimmed_cache:
            return self.dimmed_cache[key]
        cache_path = os.path.join(CACHE_DIR, f"{name}.png")
        try:
            img = Image.open(cache_path).convert("RGB")
            img = img.resize((size, size), Image.LANCZOS)
        except:
            img = self._fallback(name, size)
        img = img.convert("RGBA")
        tint_rgba = (10, 200, 185, 100) if color == "ally" else (232, 76, 76, 100)
        overlay = Image.new("RGBA", (size, size), (0, 0, 0, 160))
        img = Image.alpha_composite(img, overlay)
        tk_img = ImageTk.PhotoImage(img.convert("RGB"))
        self.dimmed_cache[key] = tk_img
        return tk_img

    def preload_all(self, progress_callback=None):
        for i, name in enumerate(ALL_CHAMPS):
            self._load_one(name, 56)
            if progress_callback:
                progress_callback(i + 1, len(ALL_CHAMPS), name)


