    def _quick_ban(self, name):
        if name in self.bans_blue or name in self.bans_red: return
        if name in self.ally.values() or name in self.enemy.values(): return
        self._push_undo()
        if len(self.bans_blue) <= len(self.bans_red):
            if len(self.bans_blue) < 5:
                self.bans_blue.append(name)
        else:
            if len(self.bans_red) < 5:
                self.bans_red.append(name)
        self._refresh()


