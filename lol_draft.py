    def _refresh_grid(self):
        q = self.search_query.lower()
        rf = self.role_filter

        ally_list = [v for v in self.ally.values() if v]
        enemy_list = [v for v in self.enemy.values() if v]
        bans_all = self.bans_blue + self.bans_red
        unavail = set(bans_all) | set(ally_list) | set(enemy_list)

        role_scores = {}
        for role in ROLES:
            if self.ally[role]: continue
            for c in champs_for_role(role):
                if c in unavail: continue
                s = 0.0
                direct_enemy = self.enemy.get(role, "")
                if direct_enemy:
                    s += matchup_score(c, direct_enemy) * 1.3
                for er, en in self.enemy.items():
                    if er != role and en:
                        s += matchup_score(c, en) * 0.7
                for al in self.ally.values():
                    if al: s += get_synergy(c, al) * 1.8
                for en in self.enemy.values():
                    if en: s -= get_synergy(en, c) * 0.4
                cur = role_scores.get(c, None)
                if cur is None or s > cur:
                    role_scores[c] = round(s, 1)

        top_by_role = {}
        for role in ROLES:
            if self.ally[role]: continue
            cands = [(c, role_scores.get(c, 0)) for c in champs_for_role(role)
                     if c not in unavail]
            cands.sort(key=lambda x: -x[1])
            top_by_role[role] = set([c for c, _ in cands[:3]])

        top_picks = set()
        for rs in top_by_role.values():
            top_picks |= rs

        for name, card in self.champ_cards.items():
            if rf != "ALL" and rf not in CHAMPS_ROLES.get(name, []):
                card.grid_remove()
                continue
            if q and q not in to_display(name).lower() and q not in name.lower():
                card.grid_remove()
                continue
            card.grid()

            for w in card.winfo_children():
                if hasattr(w, "_overlay_marker"):
                    w.destroy()

            if name in bans_all:
                img = self.portraits.get_banned(name, 56)
                if img:
                    card._name_label.configure(image=img)
                    card._name_label.image = img
                card.configure(highlightbackground=COLORS["enemy"], highlightthickness=2)
                self._set_overlay_text(card, "BANNI", COLORS["enemy"])
            elif name in ally_list:
                img = self.portraits.get_dimmed(name, 56, "ally")
                if img:
                    card._name_label.configure(image=img)
                    card._name_label.image = img
                card.configure(highlightbackground=COLORS["ally"], highlightthickness=2)
                self._set_overlay_text(card, "ALLIÉ", COLORS["ally"])
            elif name in enemy_list:
                img = self.portraits.get_dimmed(name, 56, "enemy")
                if img:
                    card._name_label.configure(image=img)
                    card._name_label.image = img
                card.configure(highlightbackground=COLORS["enemy"], highlightthickness=2)
                self._set_overlay_text(card, "ENNEMI", COLORS["enemy"])
            else:
                img = self.portraits.get(name, 56)
                if img:
                    card._name_label.configure(image=img)
                    card._name_label.image = img
                if name in top_picks:
                    score = role_scores.get(name, 0)
                    card.configure(highlightbackground=COLORS["ally"], highlightthickness=2)
                    self._set_badge(card, f"+{int(score)}" if score > 0 else f"{int(score)}",
                                     COLORS["ally"])
                else:
                    card.configure(highlightbackground=COLORS["bg_alt"], highlightthickness=2)
                    self._clear_badge(card)

    def _set_overlay_text(self, card, text, color):
        ov = tk.Label(card, text=text, bg=COLORS["bg"], fg=color,
                       font=("Segoe UI", 9, "bold"))
        ov._overlay_marker = True
        ov.place(relx=0.5, rely=0.5, anchor="center")
        ov.bind("<Button-1>", lambda e: self._on_champ_click(card._champ))
        self._clear_badge(card)

    def _set_badge(self, card, text, color):
        card._badge.configure(text=text, bg=color)
        card._badge.place(relx=1.0, rely=0.0, anchor="ne", x=-1, y=1)

